		<h1>Posty</h1>
		
		<h3>Un cliente de Twitter bajo en calorías diseñado para que "twitees" todo lo que quieras.</h3>
		<p>Básicamente, Posty te permite fácilmente "twitear" utilizando la conexión GPRS con la que ya cuentas en tu teléfono celular, consumiendo poco menos de 2 Kb.</p>
		
		<h2>Ejemplo</h2>
		
		<p>En México un SMS en promedio cuesta 85 centavos de peso mientras que una conexion GPRS de 2KB (sin plan de datos) cuesta 8 centavos.*</p>
		<p class="hl">¡Utilizando Posty ahorras hasta 90%!</p>
		<p>* Datos de Telcel</p>
		<div class="box">
			<h2>3 simples pasos</h2> 
			<ol>
				<li>En tu télefono celular agrega un "Favorito" o "Bookmark" con la siguiente dirección: <strong>http://posty.us/tu_usuario</strong></li>
				<li>Cada vez que ingreses a esta dirección , simplemente escribe tu tweet e ingresa tu contraseña.</li>
				<li>¡Da clic en "Post" y listo!</li> 
			</ol> 
		</div>
		<h2>Politícas del servicio</h2>
		<ol> 
			<li>Este servicio es provisto "tal y como está", lo que significa que no nos hacemos responsables por el comportamiento de la aplicación.</li> 
			<li>Nosotros simplemente funcionamos como canal entre "el usuario" y "Twitter" por lo que no somos responsables por las acciones de ninguno de ellos, ni por el contenido de los mensajes.</li> 
			<li>Nosotros NO almacenamos contraseñas, ni ningún tipo de información sensible de nuestros usuarios. La única información que guardamos es para fines estadísticos.</li> 
			<li>No nos responsabilizamos por tu estado de cuenta de telefonía celular. Si te vuelves adicto a twitear todo lo que ves o haces, te recomendamos que busques ayuda profesional.</li> 
			<li>Si tienes alguna duda acerca de estas políticas, te pedimos que te pongas en contacto a través de nuestro correo info@posty.us para revisar el caso de manera personal.</li> 
		</ol>
		<h2>Preguntas frecuentes</h2>
		<ol>
			<li>
				<p><strong>¿Es seguro twittear por Posty?</strong></p>
				<p>La respuesta corta es sí. La respuesta larga es, depende qué tan seguro necesites que sea tu Tweet. Los sitios web normalmente utilizan HTTP para transferir todos tus datos, excepto cuando manejan información sensible como podrías ser tu número de tarjeta de crédito, en ese caso usan HTTPS. Por el momento, Posty utiliza HTTP, pero estamos considerando implementar HTTPS en un futuro.</p>
			</li>
			<li>
				<p><strong>¿Qué gana 42claps con Posty?</strong></p>
				<p>En realidad, nada excepto la satisfacción de poder ayudar a que Twitter llegue a más personas. Al ser un cliente ligero, tampoco es un gran costo para nosotros.</p>
				
			</li>
			<li>
				<p><strong>¿Posty es Código Abierto?</strong></p>
				<p>Pues Posty es tan poco código que no pensamos que ponerlo al aire sea de gran ayuda, pero si quieres tenerlo, usarlo y contribuir a que Posty sea mejor, simplemente <a href="mailto:info@posty.us">ponte en contacto con nosotros</a>.</p>
			</li>
		</ol>
		<h2>Acerca de Posty</h2>
		<p>El producto fue desarrollado por César Salazar durante un viaje a San Francisco. Al encontrarse con el problema de tener que cuidar como oro sus Kb del plan de datos, decidió apagar su cliente de Twitter habitual y twitear a traves de uno mas ligero, o sea Posty. El resultado es que ahora César tiene un poco más dinero disponible para poder tomar café y programar en el acogedor café de San Francisco, donde todo comenzó.</p>
